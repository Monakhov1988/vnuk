export function getTopicsByCategory(categoryId: string): Topic[] {
  return TOPIC_CATALOG.filter(t => t.category === categoryId);
}

export function getTopicById(topicId: string): Topic | undefined {
  return TOPIC_CATALOG.find(t => t.id === topicId);
}

export function buildTopicPromptSection(
  enabledTopics: Array<{ topicId: string; depth: TopicDepth }>
): string {
  if (enabledTopics.length === 0) return "";

  const lines: string[] = [
    "",
    "═══════════════════════════════════════",
    "НАСТРОЙКИ ТЕМ — УРОВЕНЬ ЭКСПЕРТИЗЫ",
    "═══════════════════════════════════════",
    "",
  ];

  const byCategory = new Map<string, Array<{ topic: Topic; depth: TopicDepth }>>();

  for (const { topicId, depth } of enabledTopics) {
    const topic = getTopicById(topicId);
    if (!topic) continue;
    const arr = byCategory.get(topic.category) || [];
    arr.push({ topic, depth });
    byCategory.set(topic.category, arr);
  }

  for (const cat of TOPIC_CATEGORIES) {
    const topics = byCategory.get(cat.id);
    if (!topics || topics.length === 0) continue;

    lines.push(`${cat.icon} ${cat.name.toUpperCase()}:`);
    for (const { topic, depth } of topics) {
      const prompt = depth === "expert" ? topic.promptExpert
        : depth === "detailed" ? topic.promptDetailed
        : topic.promptBasic;
      lines.push(`• ${topic.name}: ${prompt}`);
    }
    lines.push("");
  }

  return lines.join("\n");
}

