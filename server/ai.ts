function getMoscowTime(): { hours: number; minutes: number; timeOfDay: string; day: number; month: number; year: number } {
  const now = new Date();
  const msk = new Date(now.toLocaleString("en-US", { timeZone: "Europe/Moscow" }));
  const hours = msk.getHours();
  const minutes = msk.getMinutes();
  const day = msk.getDate();
  const month = msk.getMonth();
  const year = msk.getFullYear();

  let timeOfDay: string;
  if (hours >= 5 && hours < 12) {
    timeOfDay = "утро";
  } else if (hours >= 12 && hours < 17) {
    timeOfDay = "день";
  } else if (hours >= 17 && hours < 23) {
    timeOfDay = "вечер";
  } else {
    timeOfDay = "ночь";
  }

  return { hours, minutes, timeOfDay, day, month, year };
}

function stripMarkdown(text: string): string {
  let result = text;
  result = result.replace(/^#{1,6}\s+/gm, "");
  result = result.replace(/\*\*([^*]+)\*\*/g, "$1");
  result = result.replace(/\*([^*]+)\*/g, "$1");
  result = result.replace(/__([^_]+)__/g, "$1");
  result = result.replace(/_([^_]+)_/g, "$1");
  result = result.replace(/```[\s\S]*?```/g, "");
  result = result.replace(/`([^`]+)`/g, "$1");
  result = result.replace(/^\s*[-•]\s+/gm, "• ");
  result = result.replace(/\n{3,}/g, "\n\n");
  return result.trim();
}

