function isFactualQuestion(message: string): boolean {
  const lower = message.toLowerCase().trim();

  const questionStarters = /^(кто |что |где |когда |сколько |почему |зачем |какой |какая |какое |какие |каков |куда |откуда |чем |кому |кем |как давно |с какого |до какого |в каком |на каком )/;
  if (questionStarters.test(lower)) return true;

  const factualPatterns = [
    /(?:правда (?:ли |что ))/,
    /(?:это правда)/,
    /(?:скажи |расскажи |объясни |подскажи )(?:мне )?(?:про |о |об |что |как |где |когда |почему |зачем )/,
    /(?:как (?:называется|зовут|работает|устроен|действует|пользоваться|включить|выключить|настроить|установить|подключить|сделать|убрать|отстирать|починить|вывести|очистить))/,
    /(?:что (?:такое|значит|означает|делать если|делать когда|будет если|помогает от|лучше))/,
    /(?:сколько (?:стоит|весит|длится|километров|часов|дней|лет|раз|нужно))/,
    /(?:где (?:находится|можно|лучше|ближайш|купить|найти|взять|достать))/,
    /(?:когда (?:будет|начнётся|начнется|откроется|закроется|наступит|празднуют|отмечают|сажать|сеять))/,
    /(?:есть ли |можно ли |нужно ли |стоит ли |бывает ли )/,
    /(?:в чём разница|чем отличается|чем отличаются|что лучше)/,
    /(?:симптомы|признаки|причины|лечение|профилактика|диагностика)/,
  ];
  if (factualPatterns.some(p => p.test(lower))) return true;

  const healthComplaints = /(?:болит\b|ноет\b|тянет\b|колет\b|давит\b|жжёт\b|жжет\b|немеет\b|отекает\b|опухл|кружится голова|тошнит|рвота|понос|запор|поднялась температур|температура 3[789]|температура 4\d|давление \d{2,3}\/|давление скач|не могу спать|бессонниц|задыхаюсь|одышка|сыпь\b|зуд\b|чешется)/;
  if (healthComplaints.test(lower)) return true;

  const householdQuestions = /(?:как (?:отстирать|отмыть|почистить|вывести пятно|убрать запах|починить|заклеить|отбелить|погладить|засолить|замариновать|заморозить|разморозить|хранить))/;
  if (householdQuestions.test(lower)) return true;

  const techHelp = /(?:не работает |не включается|не подключается|как (?:включить|выключить|настроить|обновить|скачать|установить|удалить|перезагрузить)|вайфай|wifi|wi-fi|интернет не |экран не |телефон не |компьютер не |принтер не |пульт не )/;
  if (techHelp.test(lower)) return true;

  const smallTalkExclusions = /^(как дела|как ты|как жизнь|как настроение|как поживаешь|как самочувстви|привет|здравствуй|добр|спасибо|пока |до свидани|спокойной ночи|хорошего дня)/;
  if (smallTalkExclusions.test(lower)) return false;

  if (lower.includes("?")) {
    const informationalQ = /(?:кто .{2,}|что .{3,}(?:такое|значит|означает|стоит|делать|будет)|где .{2,}|когда .{2,}|сколько .{2,}|почему .{2,}|какой .{2,}|какая .{2,}|какие .{2,}|куда .{2,}|откуда .{2,}|зачем .{2,}|чем .{3,}(?:отличается|лучше|полезн)|можно ли .{3,}|нужно ли .{3,}|правда (?:ли |что ))/;
    if (informationalQ.test(lower)) return true;
  }

  return false;
}

function normalizeText(text: string): string {
  return text.toLowerCase().replace(/[^a-zа-яёa-z0-9\s]/gi, "").replace(/\s+/g, " ").trim();
}

function stringSimilarity(a: string, b: string): number {
  if (a === b) return 1;
  if (!a || !b) return 0;
  const longer = a.length >= b.length ? a : b;
  const shorter = a.length < b.length ? a : b;
  if (longer.length === 0) return 1;
  const costs: number[] = [];
  for (let i = 0; i <= longer.length; i++) {
    let lastVal = i;
    for (let j = 0; j <= shorter.length; j++) {
      if (i === 0) { costs[j] = j; }
      else if (j > 0) {
        let newVal = costs[j - 1];
        if (longer[i - 1] !== shorter[j - 1]) {
          newVal = Math.min(newVal, lastVal, costs[j]) + 1;
        }
        costs[j - 1] = lastVal;
        lastVal = newVal;
      }
    }
    if (i > 0) costs[shorter.length] = lastVal;
  }
  return 1 - costs[shorter.length] / longer.length;
}

async function detectRepeatedQuestion(
  currentMsg: string,
  userId: number
): Promise<{ isRepeat: boolean; cachedReply?: string }> {
  try {
    const recentDbMessages = await storage.getChatMessages(userId, 100);
    const now = Date.now();
    const DAY_MS = 24 * 60 * 60 * 1000;
    const normalizedCurrent = normalizeText(currentMsg);
    if (normalizedCurrent.length < 5) return { isRepeat: false };

    let matchCount = 0;
    let lastMatchedReply: string | undefined;

    for (let i = 0; i < recentDbMessages.length; i++) {
      const msg = recentDbMessages[i];
      if (msg.role !== "user") continue;
      const msgTime = msg.createdAt ? new Date(msg.createdAt).getTime() : 0;
      if (now - msgTime > DAY_MS) continue;

      const normalizedMsg = normalizeText(msg.content);
      if (stringSimilarity(normalizedCurrent, normalizedMsg) > 0.8) {
        matchCount++;
        if (!lastMatchedReply) {
          for (let j = i - 1; j >= 0; j--) {
            if (recentDbMessages[j].role === "assistant") {
              lastMatchedReply = recentDbMessages[j].content;
              break;
            }
          }
        }
      }
    }

    if (matchCount >= 2 && lastMatchedReply) {
      const timeSensitiveSearch = /(?:расписание|афиша|новости|прогноз|цена|стоимость|курс валют|котировк|наличи|свободн|билет|очередь|что (?:сегодня|сейчас|завтра|вечером))/;
      if (timeSensitiveSearch.test(normalizedCurrent)) {
        console.log(`[ai] REPEAT-DETECT: repeated but TIME-SENSITIVE, skipping cache`);
        return { isRepeat: false };
      }
      console.log(`[ai] REPEAT-DETECT: question repeated ${matchCount + 1} times in 24h, returning cached answer`);
      return { isRepeat: true, cachedReply: lastMatchedReply };
    }
  } catch (err: any) {
    console.error("[ai] repeat detection error:", err.message);
  }
  return { isRepeat: false };
}

