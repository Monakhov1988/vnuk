async function executeToolCall(
  toolName: string,
  args: Record<string, string>,
  userId?: number
): Promise<{ text: string; imageUrl?: string; imageBuffer?: Buffer }> {
  switch (toolName) {
    case "get_weather": {
      if (!args.city) {
        return { text: "Пользователь не указал город. Спроси, в каком городе нужна погода." };
      }
      const result = await getWeather(args.city);
      return { text: result };
    }
    case "search_web": {
      const result = await searchWeb(args.query || "", userId);
      return { text: result };
    }
    case "search_recipe": {
      const result = await searchRecipe(args.dish || "", userId);
      return { text: result };
    }
    case "find_greeting_card": {
      const cardResult = await searchGreetingCard(args.query || "", userId);
      if (cardResult.buffer) {
        return { text: `Нашёл красивую открытку!`, imageBuffer: cardResult.buffer };
      }
      const fallback = await generateImage(args.query || "", userId);
      if (fallback.error) {
        return { text: fallback.error };
      }
      if (fallback.url && userId) {
        try {
          const resp = await fetch(fallback.url, { signal: AbortSignal.timeout(10000) });
          if (resp.ok) {
            const buf = Buffer.from(await resp.arrayBuffer());
            if (buf.length > 1000) {
              return { text: "Нарисовал открытку специально!", imageBuffer: buf };
            }
          }
        } catch {}
      }
      return { text: "Нарисовал открытку специально!", imageUrl: fallback.url || undefined };
    }
    case "generate_image": {
      const result = await generateImage(args.description || "", userId);
      if (result.error) {
        return { text: result.error };
      }
      return { text: "Картинка успешно сгенерирована.", imageUrl: result.url || undefined };
    }
    case "search_cinema": {
      const result = await searchCinema(args.city || undefined, userId);
      return { text: result };
    }
    case "search_movie": {
      const result = await searchMovie(args.query || "", userId);
      return { text: result };
    }
    case "search_place": {
      const result = await searchPlace(args.query || "", args.city || undefined, userId);
      return { text: result };
    }
    case "search_transport": {
      const result = await searchTransport(args.from || "", args.to || "", args.date || undefined, args.transport_type || undefined, userId);
      return { text: result };
    }
    case "search_clinic": {
      const result = await searchClinic(args.query || "", args.city || undefined, userId);
      return { text: result };
    }
    case "search_medicine": {
      const result = await searchMedicine(args.query || "", userId);
      return { text: result };
    }
    case "search_tv": {
      const result = await searchTV(args.channel || undefined, args.date || undefined, userId);
      return { text: result };
    }
    case "search_gov_services": {
      const result = await searchGovServices(args.query || "", userId);
      return { text: result };
    }
    case "search_garden": {
      const result = await searchGarden(args.query || "", args.region || undefined, userId);
      return { text: result };
    }
    case "search_product": {
      const result = await searchProduct(args.query || "", userId);
      return { text: result };
    }
    case "search_legal": {
      const result = await searchLegal(args.query || "", userId);
      return { text: result };
    }
    case "search_travel": {
      const result = await searchTravel(args.query || "", userId);
      return { text: result };
    }
    case "record_blood_pressure": {
      const systolic = parseInt(args.systolic);
      const diastolic = parseInt(args.diastolic);
      if (!systolic || !diastolic || systolic < 60 || systolic > 260 || diastolic < 30 || diastolic > 180) {
        return { text: "Некорректные показания давления. Попроси уточнить цифры." };
      }
      if (!userId) {
        return { text: "Не удалось определить пользователя." };
      }
      try {
        await storage.createHealthLog({
          parentId: userId,
          systolic,
          diastolic,
          note: args.note || null,
        });
        await storage.createEvent({
          userId: userId,
          parentId: userId,
          type: "health",
          severity: systolic >= 180 || diastolic >= 110 ? "critical" : systolic >= 140 || diastolic >= 90 ? "warning" : "info",
          title: "Давление записано",
          description: `${systolic}/${diastolic}${args.note ? ` — ${args.note}` : ""}`,
        });
        let assessment = "";
        if (systolic >= 180 || diastolic >= 110) {
          assessment = "ВНИМАНИЕ: давление очень высокое! Рекомендуй вызвать скорую (103) или обратиться к врачу немедленно.";
        } else if (systolic >= 140 || diastolic >= 90) {
          assessment = "Давление повышенное. Рекомендуй отдохнуть и измерить через 30 минут повторно.";
        } else if (systolic < 90 || diastolic < 60) {
          assessment = "Давление пониженное. Рекомендуй выпить воды, полежать.";
        } else {
          assessment = "Давление в пределах нормы.";
        }
        return { text: `Давление ${systolic}/${diastolic} успешно записано. ${assessment}` };
      } catch (err) {
        console.error("[ai] Failed to record blood pressure:", err);
        return { text: "Не удалось записать давление. Попробуй позже." };
      }
    }
    default:
      return { text: "Инструмент не найден." };
  }
}

