export function extractDishFromText(text: string): string | null {
  const lower = text.toLowerCase().trim().replace(/[?.!,]$/g, "");

  let dishBase = lower;
  for (const prefix of RECIPE_PREFIXES) {
    const stripped = dishBase.replace(prefix, "").trim();
    if (stripped !== dishBase) {
      dishBase = stripped;
      break;
    }
  }

  if (dishBase === lower && !lower.startsWith("рецепт")) {
    return null;
  }

  const normalized = DISH_FORMS[dishBase] || dishBase;
  return normalized;
}
